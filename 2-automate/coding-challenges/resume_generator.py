class ResumeGenerator:
    """
    Class that represents a Resume generator for that database info mentioned above.
    """
    
    def __init__(self, database):
        self._database = database
    
    def __str__(self):
        return 'Resume of {}'.format(self._database['personal_info']['name'].title())

    def extract_personal_info(self):
        text = '## Personal Information\n'
        text = text + 'Name : {}'.format(self._database['personal_info']['name'].title()) + '\n'
        text = text + 'Location : {}'.format(self._database['personal_info']['location'].title()) + '\n'
        text = text + 'Phone : {}'.format(self._database['personal_info']['phone'].title()) + '\n'
        text = text + 'Email : {}'.format(self._database['personal_info']['email'].title()) + '\n'
        text = text +'\n'
        return text

    def extract_objective(self):
        text = '## Objective\n'
        text = text + self._database['objective'] +'\n'
        text = text +'\n'
        return text

    def extract_education(self):
        text = '## Education\n'
        for edu in self._database['education']:
            text = text + '#### ' + edu['institution'] + ', ' + edu['location'] + ', ' + str(edu['year']) + '\n'
            text = text + '- Completed degree in ' + edu['degree'] + '\n'
            text = text + '- ' + edu['description'] + '\n'
        text = text +'\n'
        return text

    def extract_employement(self):
        text = '## Employement Highlights\n'
        for emp in self._database['employment']:
            text = text + '#### ' + emp['role'] + ', ' + str(emp['year']) + '\n'
            text = text + emp['company'] + ', ' + emp['location'] + '\n'
            for desc in emp['description']:
                text = text + '- ' + desc + '\n'
            text = text +'\n'
        text = text +'\n'
        return text

    def extract_skills(self):
        text = '## Skills and Abilities\n'
        for skill in self._database['skills']:
            text = text + '- ' + skill + '\n'
        text = text +'\n'
        return text
        
    def extract_activities(self):
        text = '## Activities\n'
        for activity in self._database['activities']:
            text = text + '- ' + activity + '\n'
        text = text +'\n'
        return text
    
    def extract_hobbies(self):
        text = '## Hobbies\n'
        for hobby in self._database['hobbies']:
            text = text + '- ' + hobby + '\n'
        text = text +'\n'
        return text
    
    def show_resume_content(self):
        text = '# Resume \n\n'
